package com.example.mid.dto;

public class Analytics {
    private int registeredCount;
    private int attendedCount;
    private int notAttendedCount;
    private int registeredUserCount;
    private int attendedUserCount;
    private int notAttendedUserCount;
    private int registeredGuestCount;
    private int attendedGuestCount;
    private int notAttendedGuestCount;


    public int getRegisteredCount() { return registeredCount; }

    public void setRegisteredCount(int registeredCount) { this.registeredCount = registeredCount; }

    public int getAttendedCount() { return attendedCount; }

    public void setAttendedCount(int attendedCount) { this.attendedCount = attendedCount; }

    public int getNotAttendedCount() { return notAttendedCount; }

    public void setNotAttendedCount(int notAttendedCount) { this.notAttendedCount = notAttendedCount; }

    public int getRegisteredUserCount() { return registeredUserCount; }

    public void setRegisteredUserCount(int registeredUserCount) { this.registeredUserCount = registeredUserCount; }

    public int getAttendedUserCount() { return attendedUserCount; }

    public void setAttendedUserCount(int attendedUserCount) { this.attendedUserCount = attendedUserCount; }

    public int getNotAttendedUserCount() { return notAttendedUserCount; }

    public void setNotAttendedUserCount(int notAttendedUserCount) { this.notAttendedUserCount = notAttendedUserCount; }

    public int getRegisteredGuestCount() { return registeredGuestCount; }

    public void setRegisteredGuestCount(int registeredGuestCount) { this.registeredGuestCount = registeredGuestCount; }

    public int getAttendedGuestCount() { return attendedGuestCount; }

    public void setAttendedGuestCount(int attendedGuestCount) { this.attendedGuestCount = attendedGuestCount; }

    public int getNotAttendedGuestCount() { return notAttendedGuestCount; }

    public void setNotAttendedGuestCount(int notAttendedGuestCount) { this.notAttendedGuestCount = notAttendedGuestCount; }
}
